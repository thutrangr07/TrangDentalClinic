const header = document.querySelector(".site-header");
const menuToggle = document.querySelector(".menu-toggle");
const mobileNav = document.querySelector(".mobile-nav");
const langButtons = document.querySelectorAll(".lang-button");
const appointmentForm = document.getElementById("appointment-form");
let currentLanguage = "vi";

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.vi;
  currentLanguage = lang;

  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });

  localStorage.setItem("tdc-language", lang);
}

function updateHeader() {
  header.dataset.elevated = String(window.scrollY > 24);
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

menuToggle.addEventListener("click", () => {
  const isOpen = document.body.classList.toggle("menu-open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

mobileNav.addEventListener("click", (event) => {
  if (event.target.matches("a")) {
    document.body.classList.remove("menu-open");
    menuToggle.setAttribute("aria-expanded", "false");
  }
});

window.addEventListener("scroll", updateHeader, { passive: true });

appointmentForm.addEventListener("submit", (event) => {
  event.preventDefault();
  const dictionary = translations[currentLanguage] || translations.vi;
  const formData = new FormData(appointmentForm);
  const details = [
    `${dictionary.formName}: ${formData.get("name") || ""}`,
    `${dictionary.formPhone}: ${formData.get("phone") || ""}`,
    `${dictionary.formService}: ${formData.get("service") || ""}`,
    `${dictionary.formMessage}: ${formData.get("message") || ""}`
  ].join("\n");

  const mailto = new URL("mailto:hello@trangdentalclinic.com");
  mailto.searchParams.set("subject", dictionary.emailSubject);
  mailto.searchParams.set("body", details);
  window.location.href = mailto.toString();
});

document.getElementById("year").textContent = new Date().getFullYear();
setLanguage(localStorage.getItem("tdc-language") || "vi");
updateHeader();
